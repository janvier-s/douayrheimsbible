	<h2>The Choice That Defined the Translation</h2>

	<p>
		The Douay-Rheims <a href="https://en.wikipedia.org/wiki/Vulgate">Latin Vulgate</a> translation is
		the result of a deliberate and theologically grounded choice. Gregory Martin and his colleagues at
		Rheims faced a foundational question before they wrote a single word: which text should they translate
		from? The Protestant Bibles that had flooded England for the previous half-century, including Tyndale's,
		Coverdale's, and the Geneva Bible, were all made directly from the Hebrew Old Testament and the Greek
		New Testament. Their translators treated the original languages as the ultimate authority, and their
		work carried the freshness that comes from that decision.
	</p>

	<p>
		Martin took a different path. He translated from Saint Jerome's Latin Vulgate, the version that
		had been the Bible of the Western Church for eleven centuries. When scholars pointed to the
		Greek manuscripts and suggested their rendering was more accurate, the response of the Rheims
		translators was direct: "The Latin Vulgate was the more preferable version, as it was the
		version of the Church, while the Greek text had for centuries been in the hands of heretics, and
		subject to their corruptions."
	</p>

	<p>
		The choice was a theological argument, grounded in the Council of Trent's declaration that the
		Vulgate was "authentic," the only text to be used in public readings, disputations, and Church
		proceedings. For Martin and Allen, this was dispositive. The Church was the custodian of
		Scripture; her Bible was the Bible.
	</p>

