	<h2>The Choice That Defined the Translation</h2>

	<p>
		Gregory Martin and his colleagues at Rheims faced a foundational question before they wrote a
		single word of their translation: which text should they translate from? The Protestant Bibles
		that had flooded England for the previous half-century, including Tyndale's, Coverdale's, and
		the Geneva Bible, were all made directly from the Hebrew Old Testament and the Greek New
		Testament. Their translators treated the original languages as the ultimate authority, and their
		work carried the freshness that comes from that decision.
	</p>

	<p>
		Martin took a different path. He translated from Jerome's Latin Vulgate, the version that had
		been the Bible of the Western Church for eleven centuries. When scholars pointed to the Greek
		manuscripts and suggested their rendering was more accurate, the response of the Rheims
		translators was direct: "The Latin Vulgate was the more preferable version, as it was the
		version of the Church, while the Greek text had for centuries been in the hands of heretics, and
		subject to their corruptions."
	</p>

	<p>
		The choice was a theological argument, grounded in the Council of Trent's declaration that the
		Vulgate was "authentic," the only text to be used in public readings, disputations, and Church
		proceedings. For Martin and Allen, this was dispositive. The Church was the custodian of
		Scripture; her Bible was the Bible.
	</p>

	<h2>The Title Page States It Plainly</h2>

	<p>
		The Rheims New Testament announced this choice on its cover:
		<q
			>The New Testament of Jesus Christ translated faithfully into English out of the authentical
			Latin according to the best corrected copies of the same diligently conferred with the Greek
			and other editions in divers languages.</q
		>
		The Douay Old Testament said the same:
		<q>The Holie Bible faithfully translated into English out of the authentical Latin.</q>
	</p>

	<p>
		The word "authentical" was doing a great deal of work. The translators did compare their text
		against the Greek and other editions, but always with the Vulgate as the governing authority.
		Where the Greek and the Latin diverged, the Latin prevailed.
	</p>

	<h2>When the Latin Was Unclear</h2>

	<p>
		The price of this decision became visible at certain passages where the Latin text itself was
		obscure. Psalm 57:10 in the Rheims reads: "Before your thorns did understand the old Briar; as
		living so in wrath he swalloweth them." Bishop Westcott, examining the passage much later, found
		it impossible to construe; it had been rendered literally from a corrupted Latin text. The
		Revised Version of 1881 would eventually make the passage intelligible: the image is of a
		cooking fire scattered by desert wind before it heats the pot. Martin's rendering follows the
		Latin faithfully; the Latin, in this instance, had ceased to make sense.
	</p>

	<p>
		This was the unavoidable consequence of his method. Mombert's summary was blunt: the Douay Bible
		was "a faithful translation of the revision of a bad Latin version made from the Greek
		translation of the Hebrew original": a translation three removes from its source.
	</p>

	<h2>A Deliberate Philosophy</h2>

	<p>
		Hoare, examining the strange coinages that fill the translation, drew a conclusion that has
		survived scholarly scrutiny. "No man could be better aware than a scholarly Englishman like
		Gregory Martin that such renderings as these were simply barbarous." The Latinisms were a
		policy, not an accident.
	</p>

	<p>
		The passage from Ephesians illustrates this most vividly. Chapter three, verses eight through
		ten, in the Rheims:
		<q
			>To me the least of all the sainctes is given this grace, among the Gentils to euangelise the
			unsearchable riches of Christ and to illuminate al men what is the dispensation of the
			sacrament hidden from worlds in God, who created al things; that the manifold wisdom of God
			may be notified to the Princes and Potestats in the Celestials by the Church, according to the
			prefinition of worlds which he made in Christ Jesus our Lord.</q
		>
		Every choice preserves the Latin term;
		<q>Potestats,</q> <q>prefinition,</q> and <q>sacrament</q> each anchor the English reader to the Vulgate
		and prevent any drift toward Protestant equivalents.
	</p>

	<p>
		Among the other characteristic coinages: "commestation," "contristate," "odible," "exinanited
		himself" (for the self-emptying of Philippians 2:7), "Purge the old leaven that you may be a new
		paste, as you are azymes" (for 1 Corinthians 5:7). Martin knew these words were barbarous. He
		used them because they preserved the theological precision of the Latin. Where a smooth English
		rendering might have introduced ambiguity, he preferred the difficulty of Latin transparency.
	</p>

	<h2>What the Fidelity Gave</h2>

	<p>
		The same method that produced the Latinisms also produced some of the finest translations in the
		collection:
	</p>

	<ul>
		<li><q>By their fruits you shall know them</q> (Matthew 7:16)</li>
		<li><q>Liberty of the glory of the children of God</q> (Romans 8:21)</li>
		<li><q>Holiness of the truth</q> (Ephesians 4:24)</li>
	</ul>

	<p>
		In the first chapter of St James alone, the Rheims gave English its "upbraideth not," its
		"engrafted word," its "bridleth not," all adopted by the King James translators.
	</p>

	<p>
		Moulton found more than forty instances where the Rheims New Testament alone, among all English
		versions from Tyndale to the Authorized Version, correctly represents the Greek Article. The
		care these Oxford classicists brought to the Latin text was real scholarship, even when its
		results were unreadable.
	</p>

	<p>
		Hoare extended the argument to something larger: the Vulgate itself. Jerome's Latin, he argued,
		was <q
			>the venerable source from which the Church has drawn the largest part of its ecclesiastical
			vocabulary.</q
		> Terms now so familiar as to be taken for granted all derive from Jerome's translation:
	</p>

	<ul>
		<li><q>Scripture</q></li>
		<li><q>communion</q></li>
		<li><q>grace</q></li>
		<li><q>sanctification</q></li>
		<li><q>justification</q></li>
		<li><q>salvation</q></li>
		<li><q>penance</q></li>
		<li><q>sacrament</q></li>
		<li><q>eternity</q></li>
	</ul>

	<p>
		Westcott, who had little sympathy for the Douay method overall, conceded the vocabulary
		argument: the language of the translation, whatever its stylistic failures, "is enriched by the
		bold reduction of innumerable Latin words to English service." The Rheims translators were the
		most rigorous carriers of Jerome's vocabulary into English, and through their influence on the
		King James translators, those terms became the common inheritance of English-speaking
		Christianity.
	</p>

	<h2>A Measured Assessment</h2>

	<p>
		Hoare's conclusion was measured and fair. The Rheims edition holds "fast to the principle of
		uniformity in its renderings whenever this principle is not prejudicial to the sense. Moreover,
		for serious students, it is just the uncompromising fidelity of the translators to their
		Vulgate, which in its New Testament carries us back to the Old Latin rendering of Greek
		manuscripts current in the middle of the second century, that gives to the Rheims Edition so
		considerable a value for the purposes of textual criticism. But were we under no other
		obligation to the editors than that they helped to encourage a better acquaintance with Jerome's
		Vulgate, our debt to them would still be great."
	</p>

	<p>
		The Douay-Rheims Bible was frequently obscure, occasionally barbarous, and persistently
		difficult. It was also, in its way, a monument: to the Vulgate tradition, to the vocabulary
		Jerome gave the Church, and to the willingness of its translators to sacrifice readability in
		the service of a fidelity they considered non-negotiable.
	</p>

