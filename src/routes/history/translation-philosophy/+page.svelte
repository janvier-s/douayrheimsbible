	<h2>What the Fidelity Gave</h2>

	<p>
		The same method that produced the Latinisms also produced some of the finest translations in the
		collection:
	</p>

	<ul>
		<li><q>By their fruits you shall know them</q> (Matthew 7:16)</li>
		<li><q>Liberty of the glory of the children of God</q> (Romans 8:21)</li>
		<li><q>Holiness of the truth</q> (Ephesians 4:24)</li>
	</ul>

	<p>
		In the first chapter of St James alone, the Rheims gave English its "upbraideth not," its
		"engrafted word," its "bridleth not," all adopted by the King James translators.
	</p>

	<p>
		The care these Oxford classicists brought to the Latin text was real scholarship, even when its
		results were unreadable.
	</p>

	<p>
		Hoare extended the argument to something larger: the Vulgate itself. Saint Jerome's Latin, he
		argued, was <q
			>the venerable source from which the Church has drawn the largest part of its ecclesiastical
			vocabulary.</q
		> Terms now so familiar as to be taken for granted all derive from Saint Jerome's translation:
	</p>

	<ul>
		<li><q>Scripture</q></li>
		<li><q>Communion</q></li>
		<li><q>Grace</q></li>
		<li><q>Sanctification</q></li>
		<li><q>Justification</q></li>
		<li><q>Salvation</q></li>
		<li><q>Penance</q></li>
		<li><q>Sacrament</q></li>
		<li><q>Eternity</q></li>
	</ul>

	<p>
		Westcott, who had little sympathy for the Douay method overall, conceded the vocabulary
		argument: the language of the translation, whatever its stylistic failures, "is enriched by the
		bold reduction of innumerable Latin words to English service." The Rheims translators were the
		most rigorous carriers of Saint Jerome's vocabulary into English.
	</p>

	<h2>A Measured Assessment</h2>

	<p>
		Hoare's conclusion was measured and fair: the Rheims edition's uncompromising fidelity to the
		Vulgate, whatever its stylistic cost, carries the reader back to the Old Latin rendering of
		Greek manuscripts current in the middle of the second century, giving it a value for textual
		criticism that no later revision preserves. As <a href="/history/challoner"
			>The Challoner Revision</a
		> shows in detail, subsequent revisions would begin to smooth away this character.
	</p>

	<p>
		The Douay-Rheims Bible was frequently obscure, occasionally barbarous, and persistently
		difficult. It was also, in its way, a monument: to the Vulgate tradition, to the vocabulary
		Saint Jerome gave the Church, and to the willingness of its translators to sacrifice readability
		in the service of a fidelity they considered non-negotiable. This commitment to Jerome's Latin
		would later shift, as explored further in <a href="/history/original-tongues"
			>From the Authentic Latin to the Original Tongues</a
		>, toward translations that drew directly from the Hebrew and Greek.
	</p>

