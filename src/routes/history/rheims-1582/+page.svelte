	<h2>England in 1582</h2>

	<p>
		When the Rheims New Testament appeared in 1582, England was a country in a state of declared
		emergency. The previous year, Edmund Campion, a Jesuit priest and former Fellow of St John's
		College, Oxford, had been arrested, subjected to torture in the Tower of London, and executed at
		Tyburn. The papal excommunication of Queen Elizabeth, issued by Pope Pius V in 1570, had placed
		every English Catholic in an impossible position: loyalty to Rome was framed as treason, and
		treason carried the death sentence. Along the Atlantic coasts and in the harbours of Spain, the
		fleet that would become the Armada was being assembled. England's fate, many felt on both sides
		of the confessional divide, hung in the balance.
	</p>

	<p>
		It was into this world that the Divines of the English College at Rheims released the first
		Catholic English New Testament. Henry Barker, writing three centuries later, observed that "the
		whole feeling of England was anti-Roman." The translators knew it. They published anyway.
	</p>

	<h2>The Approbation</h2>

	<p>
		The volume arrived with a formal Approbation signed by four doctors of the Church at Rheims. In
		measured Latin, it declared that the translation contained nothing contrary to Catholic doctrine
		and nothing contrary to civil peace, and that it served the true faith, the common good, and the
		integrity of Christian life. The four signatories were:
	</p>
	<ul>
		<li>Pierre Remigius, Archdeacon of Rheims</li>
		<li>Hubert Morus, Dean of the Rheims Chapter</li>
		<li>Johannes le Bespits, Theologian and Chancellor of the University of Rheims</li>
		<li>Guillaume Balbus, Professor of Theology at the Rheims College</li>
	</ul>

	<p>
		Without this Approbation the translation had no standing in Catholic eyes. It also marked a
		claim: the Rheims version was the legitimate, authorized word of God in English, against every
		Protestant accusation to the contrary. The notes that accompanied the text left little to the
		imagination. They defended Catholic doctrine at every disputed point, attacked Protestant
		translations with considerable force, and spared neither Queen Elizabeth nor the Reformers of
		Germany, Switzerland, and France. Dr. Geddes, himself a Catholic priest, later described the
		annotations as "virulent," directed against the Protestant religion and calculated to support a
		system, as he put it, of "transalpine Popery."
	</p>

	<h2>Martin's Companion Work</h2>

	<p>
		Gregory Martin, the principal translator, had been preparing a companion polemic alongside the
		New Testament. His <em
			>Discovery of the Manifold Corruptions of the Holy Scriptures by the Heretics of Our Days</em
		>, published the same year as the New Testament though somewhat later in 1582, laid out his case
		against Protestant translations at length. The preface to the New Testament itself referred
		readers to this work: "more at large in a book lately made purposely of that matter, called a
		Discovery."
	</p>

	<p>
		Martin wrote as a man in exile, translating in the knowledge that his work was a capital offense
		in his own country and that those who carried it there faced imprisonment or worse. Elizabeth's
		government treated the Rheims New Testament as a seditious document. Priests found carrying
		copies were imprisoned. The Crown's searchers seized copies wherever they were found. Those who
		circulated the New Testament faced, in some cases, torture.
	</p>

	<h2>The Question of Access</h2>

	<p>
		Even as they produced an English New Testament, the translators required Catholic readers to
		obtain a special license before reading it. As Paris Simms later observed, "no Catholic was
		permitted to read it until he had obtained a special license from the proper authorities." The
		Church's longstanding position on vernacular Scripture, and the theology behind that
		requirement, is taken up in a later article in this series.
	</p>

	<p>
		Their stated motive for producing the translation at all was not a desire to place Scripture in
		every Catholic hand. It was the extensive circulation of Protestant English Bibles. As the
		preface explained, "the growing demand for such among Catholics finally forced them to provide
		an English translation."
	</p>

	<h2>The Protestant Counter-Offensive</h2>

	<p>
		Protestant England responded with vigour. William Fulke, Master of Pembroke Hall at Cambridge,
		published his <em>Defense of the Sincere and True Translations of the Holy Scriptures</em> in
		1583, a direct and rapid answer to Martin's <em>Discovery of Corruptions</em>. Thomas
		Cartwright, engaged at the instigation of Secretary Walsingham, was commissioned to prepare a
		more systematic refutation of the New Testament itself, though Archbishop Whitgift eventually
		prohibited him from continuing and transferred the task to Fulke. In 1585, Thomas Bilson, Warden
		of Winchester, published a further response addressing the political theology the Rheims
		translators had woven into their annotations.
	</p>

	<h2>The Refutation That Spread It</h2>

	<p>
		The most consequential response was Fulke's 1589 parallel edition, which printed the entire
		Rheims New Testament alongside the Bishops' Bible and argued against the Catholic annotations
		one by one. Passing through four printings, it placed the Rheims text in Protestant hands across
		England; by the time the King James translators gathered in 1604, it was among the most widely
		circulated New Testaments in the country. The consequences of that distribution are taken up in
		the next article.
	</p>

