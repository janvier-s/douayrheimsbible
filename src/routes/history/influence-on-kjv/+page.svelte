	<h2>The Specific Borrowings</h2>

	<p>The debt repays close attention. A sample from three chapters, taken by chance:</p>
	<ul>
		<li>Matthew 26:26 <q>blessed</q> appears first in the Rheims; the KJV keeps it.</li>
		<li>Matthew 26:30 <q>hymn</q> is the Rheims word; the KJV follows.</li>
		<li>Matthew 26:63 <q>adjure</q> enters English through the Rheims; the KJV follows.</li>
		<li>James 1:5 <q>upbraideth not</q> appears first in the Rheims and passes into the KJV.</li>
		<li>James 1:21 <q>engrafted word</q> is the Rheims phrase; the KJV adopts it.</li>
		<li>James 1:26 <q>bridleth not</q> is the Rheims word; the KJV follows.</li>
	</ul>
	<p>
		As Barker noted, examining those three chapters yielded such results that <q
			>the reader will not doubt that very many examples of the same description might be produced.</q
		>
	</p>

	<h2>Borrowings from the Epistles</h2>

	<p>
		The same pattern extends through the Epistles. Ward Allen's research identified Rheims influence
		across the Pauline letters, where the King James translators repeatedly considered and adopted
		Rheims readings in their revision notes:
	</p>

	<ul>
		<li>
			Romans 5:11: The Rheims gives "reconciliation," translating the Vulgate's "reconciliationem."
			The KJV follows.
		</li>
		<li>
			1 Corinthians 13:12: The Rheims gives "face to face" for Jerome's "facie ad faciem." The KJV
			follows.
		</li>
		<li>
			Hebrews 11:1: The Rheims gives "substance of things to be hoped for" for "substantia
			sperandarum rerum." The KJV's "substance of things hoped for" is nearly identical.
		</li>
		<li>
			1 Corinthians 13:3: The Rheims gives "charity" for "caritatem" consistently throughout the
			chapter. The KJV follows in every instance.
		</li>
	</ul>

	<p>
		In each case, the King James translators had access to the Rheims text through Fulke's 1589
		parallel edition, and the evidence of their revision notes shows them explicitly considering and
		frequently adopting Rheims readings. The Catholic translation's influence on the Protestant
		masterpiece is not a matter of inference but of documented revision history.
	</p>

	<p>
		Cardinal Newman later observed that the relationship between the two traditions was closer than
		either tradition liked to acknowledge. The KJV translators named the Rheims New Testament in
		their preface. They drew on it extensively. The translation made by Catholic scholars in exile,
		illegal in England, circulated primarily through a Protestant refutation, had shaped the
		language of the most widely read English Bible in history.
	</p>

	<h2>The Greek Article</h2>

	<p>
		The Rheims New Testament's handling of the Greek Article drew admiration from scholars long
		after its publication. William Moulton, examining the text carefully, found more than forty
		instances where the Rheims New Testament alone, among all English versions from Tyndale to the
		Authorized Version, correctly represents the Greek Article. He noted that this was remarkable
		given that the other translators were certainly known to and used by the Rheims scholars: "They
		make no allusion in their preface to any indebtedness to preceding translators, but of the fact
		there can be no doubt."
	</p>

	<p>
		This precision in handling the Article was not a stylistic accident. Gregory Martin and his
		colleagues were Oxford-trained classicists working from a text they knew intimately. Their
		attention to the Greek original, even while formally translating from the Latin Vulgate, gave
		their New Testament a scholarly rigour that Protestant translators found useful even as they
		dismissed the translation as a whole.
	</p>

	<h2>The Vocabulary Legacy</h2>

	<p>
		Beyond individual phrases, the Rheims New Testament contributed something more lasting:
		vocabulary. The words through which English speakers discuss Christian faith entered the
		language through Saint Jerome's Vulgate, and entered English through translations that followed
		the Vulgate closely. H. H. Hoare, writing on the English Bible versions, listed the terms that
		Saint Jerome gave to the Church: "Scripture, communion, grace, sanctification, justification,
		spirit, salvation, glory, congregation, penance, propitiation, conversion, election, sacrament,
		elements, discipline, eternity." All come from Saint Jerome's Bible. The Rheims translators were
		the most rigorous carriers of this vocabulary into English, and through their influence on the
		KJV translators, these terms became the common inheritance of English-speaking Christianity.
	</p>

	<p>
		Hoare drew a fair conclusion: "were we under no other obligation to the editors than that they
		helped to encourage a better acquaintance with Saint Jerome's Vulgate, our debt to them would
		still be great."
	</p>

	<h2>A Measured Assessment</h2>

	<p>
		The assessment of Gregory Martin's work that has best survived the passage of time belongs to
		William Moulton, who examined it without partisanship. "Nothing is easier," he observed, "than
		to accumulate instances of the eccentricity of this version, of its obscure and inflated
		renderings; but only minute study can do justice to its faithfulness and to the care with which
		the translators executed their work. Every other English version is to be preferred to this, if
		it must be taken as a whole; no other English version will prove more instructive to the student
		who will take the pains to separate what is good and useful from what is ill-advised and wrong."
	</p>

	<p>
		H. H. Hoare, arriving at the same conclusion from a different direction, wrote that "the Douai
		Version has one great merit, which is wanting in our Authorized Version, namely, that it holds
		fast to the principle of uniformity in its renderings whenever this principle is not prejudicial
		to the sense. Moreover, for serious students, it is just the uncompromising fidelity of the
		translators to their Vulgate, which in its New Testament carries us back to the Old Latin
		rendering of Greek manuscripts current in the middle of the second century, that gives to the
		Rheims Edition so considerable a value for the purposes of textual criticism."
	</p>

